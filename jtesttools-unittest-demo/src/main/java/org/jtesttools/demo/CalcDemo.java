package org.jtesttools.demo;

public class CalcDemo {

    public double mult(double n1, double n2) {
        return n1 * n2;
    }

    public double sub(double n1, double n2) {
        return n1 - n2;
    }

    public double div(double n1, double n2) {
        return n1 / n2;
    }

    public double sum(double n1, double n2) {
        return n1 + n2;
    }

}
